import UIKit.UIColor

extension UIColor {
    
    static var warmBlack: UIColor {
        return .init(red: 0.29, green: 0.29, blue: 0.28, alpha: 1.00)
    }
    
    static var warmGray: UIColor {
        // .init(red: 89/255, green: 82/255, blue: 96/255, alpha: 1.00)
        return .lightGray
    }
}
